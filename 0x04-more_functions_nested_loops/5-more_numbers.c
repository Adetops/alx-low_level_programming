#include "main.h"

/**
 * more_numbers - prints 10 times the numbers, from 0 to 14
 * followed by a new line
 * You can only use _putchar three times in your code
 */

void more_numbers(void)
{
	int i, j;

	i = 0;
	for (i == 0; i <= 14; i++)
	{
		for (j == 0; j <= 9; j++)
		{
			_putchar(i);
			_putchar('\n');
		}
	}
}
